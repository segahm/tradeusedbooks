			<a name="q0"></a><b>What is TradeUsedBooks.com?</b>
			<p>Tradeusedbooks.com is an online message board that allows college 
			students to advertise their textbooks to the whole college 
			community. </p>
			<p><a name="q1"></a><b>How does it work?</b></p>
			<p>It works by connecting college students through their location 
			and textbook preferences. We believe that the college community is 
			large enough to establish its own market exchange for textbooks. The 
			concept is rather simple - one group of students needs to get rid 
			textbooks while the other looks for the cheapest prices to acquire 
			those same books - so why not simply buy those, otherwise expensive, 
			textbooks from fellow students?</p>
			<a name="q2"></a><b>How do I register on tradeusedbooks.com?</b><p>
			When you click on either of the links that require authentication 
			(such as &quot;My Account&quot;) you will have an option to register. After 
			clicking on the register button you will be asked for your username 
			and email. The name that you specify for username is what will be 
			used to display you as a seller/buyer to tradeusedbooks.com 
			visitors/users. In case of email address, it is very important that 
			you specify a valid email address since we will send you a temporary 
			password and a link that you will have to follow in order for us to 
			validate your registration. Please note that all non-validated 
			registrations are automatically removed from our system after 3 days 
			- so please read your email as soon as possible. Once you follow the 
			confirmation link in your email you will be required to enter your 
			username and the temporary password you were provided with. </p>
			<p><a name="q3"></a><b>Why do you need my email address?</b></p>
			<p>We need your email address so that we can provide it as contact 
			information to those tradeusedbooks.com users that you are dealing 
			with. Please note that we do not share your email address with any 
			third party and it is simply against our moral principles (please 
			visit our privacy policy for more information) </p>
			<p><a name="q4"></a><b>How do prices on tradeusedbooks.com compare 
			to those of bookstores and other websites?</b></p>
			<p>On tradeusedbooks.com you deal with the buyer/seller directly - 
			this means that you don't have to pay any middleman fees and any 
			additional costs associated with shipping. Here are just some facts 
			about the benefits of trading on tradeusedbooks.com</p>
			<ul>
				<li>No typical rip off by the bookstore</li>
				<li>Save money on shipping and middleman fees</li>
				<li>Prices are determined by competitiveness of the market at 
				your college (free market economy)</li>
				<li>It is way faster than shipping - which usually takes no less 
				than a week (unless you use express shipping which is expensive)</li>
			</ul>
			<p><a name="q5"></a><b>How do I buy books?</b></p>
			<p>Begin by searching the textbook using our search tool. We 
			have created a search tool that automatically looks up the content of 
			your search in title, author and ISBN; it also determines what words 
			are considered to be more important and undermines the value of 
			irrelevant words. After you find the book simply click on its title 
			and you will be presented with the full description of the book. 
			This description will partly contain both the information that the 
			seller provided and the information that we were able to acquire 
			from our own sources based on ISBN. Once you click on the &quot;buy&quot; 
			button you will be asked to authenticate yourself or register. If you don't have 
			an account you will have to register first and then go through the 
			process again. However, if you are an already registered tradeusedbooks.com 
			user, your transaction is completed and all is left for you to do is 
			to wait for the seller to contact you about the means to sell you 
			the book. </p>
			<p><a name="q6"></a><b>How do I sell books?</b></p>
			<p>Selling textbooks is simple as ever on tradeusedbooks.com. First, 
			you should become confident about the right price (this can be done 
			by searching similar books on our site). Then it is 
			important that you have your book in front of you so that you will 
			have somewhere to look for any information required about the book 
			(such as title and author's name). Once you have everything prepared 
			click on the &quot;Post to Books&quot; link located on the home page. There you 
			will be asked to authenticate yourself. Once you sign in, you will 
			be asked to fill out certain information about the book. It is 
			important that you enter all the specified fields as close to the 
			reality as possible - even the optional fields can be sometimes used 
			to search an item (especially ISBN). Thus, the more you enter, the 
			more likely your book to be found later on by other users. These 
			fields should not contain any links and nor will any links be 
			displayed if entered. You should be aware of the fact that what you 
			enter is what will show up on the website, thus if you decide to 
			enter everything in lower case - that is how it will appear in the 
			end.</p>
