<p><b>So what is 
tradeusedbooks.com?</b></p>
<p style="margin-top: 0; margin-bottom: 0;font-face:Verdana;font-size: 11pt">
TradeUsedBooks is a web site, founded by UCSC students, that is dedicated to promoting textbook exchange at every college. We ourselves have been looking for ways to 
trade books for a long time and finally decided that there is a definitive need 
for tradeusedbooks.com within college communities. After all, we all have gone 
through the miserable practice of standing in the buyback line for over twenty 
minutes and receiving less than ten percent of the original price. And if that 
wasn&#39;t enough, at the beginning of every academic quarter we have to spend 
sometimes up to $400 on these textbooks (that often become obsolete by the end 
of the quarter). We all heard those stories about how textbooks&#39; editions change 
every few quarters, and some of us at one time or another found ourselves with 
an old edition that no bookstore would ever accept for a reasonable price. As 
for Amazon and eBay, these web sites require time (shipment takes up to 
two weeks), commitment, and risk. So what is the solution - why not trade textbooks on campus 
with our fellow colleagues? And so we have created tradeusedbooks.com – a web site for a 
new generation of college students!<br>
sincerely,<br>
<font face="Monotype Corsiva">tradeusedbooks.com team</font></p>
