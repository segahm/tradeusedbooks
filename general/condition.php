<p style='font-family: Courier New;'>This guideline is meant to provide both parties 
						(sellers and buyers) with a condition standard for 
						trading a book. Sellers must follow this guideline when 
						posting (and later selling) their book. If the condition 
						is not met, buyers have the right to return the book back 
						to the seller for a full refund (unless the buyer used 
						the book prior to the return). If found that the seller 
						repeatedly violated the guideline, the seller&#39;s id will 
						be removed from the system. </p>
						<table cellSpacing='0' cellPadding='5' width='100%' border='0'>
							<tr vAlign='top' style='font-size:12pt'>
								<td><font color='#FF9933'><b>NEW</b>:</font></td>
								<td width='450'>A brand-new, unused, unread copy in perfect 
								condition. Perhaps, the copy that the bookstore 
								won&#39;t buy back because it is an old edition.</td>
							</tr>
							<tr vAlign='top' style='font-size:12pt'>
								<td><font color='#FF0000'><b>LIKE NEW:</b></font></td>
								<td width='450'>A copy that has been read, but remains in 
								excellent condition. It should not have any 
								highlights and nor should it have any damages as 
								a whole. It must include all 
								of the original materials (such as CD).</td>
							</tr>
							<tr vAlign='top' style='font-size:12pt'>
								<td><font color='#009933'><b>GOOD</b>:</font></td>
								<td width='450'>A copy that has been read, but remains in 
								clean condition. All pages are intact, and the 
								cover is intact (including dust cover, if 
								applicable). The spine may show signs of wear. 
								Pages can include limited notes and 
								highlighting. May not include some of the 
								original materials (such as CD).</td>
							</tr>
							<tr vAlign='top' style='font-size:12pt'>
								<td><font color='#003300'><b>ACCEPTABLE</b>:</font></td>
								<td width='450'>A readable copy. All pages are intact and 
								may include elaborate markings or highlightings, 
								though not obscuring the original text. The book 
								may show signs of water damage. May not include 
								all of the original materials (such as CD).</td>
							</tr>
